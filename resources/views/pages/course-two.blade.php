                <h2 style="font-weight: bold;">Five Techniques Can Help To Calm The Caller, Enhance The Productiveness Of The Call, Decrease Handle Times, And Increase Customer Service Quality.</h2>
            </div>
            <div class="col-12" style="padding: 2rem;">
                <h3 class="h4">Step 1: Listen.</h3>
                <p>You can usually determine whether or not the caller is angry within the first few seconds of a call. When you come across an angry caller, it is important to simply listen to them before trying to defuse the situation. The caller may just want to vent his frustration. Sometimes, after a caller is given the chance to express displeasure, they will apologize and allow you to solve the problem with no further anger.</p>

                <p>Throughout the conversation, it may be helpful to jot down the major points of of the customer’s concerns on your notepad. Irate customers tend to go on tangents. Keeping notes will help you to find an answer to the problem more efficiently and will also allow you to maintain your focus throughout the customer conversation.</p>

                <p>If you run into an especially difficult circumstance and the angry customer is threatening you, using violent language or swearing, follow your company’s policy guidelines. Your organization should have specific rules pertaining to situations when the support team has to handle exceptionally angry customers and things get out of hand. If your guidelines allow you to end the call when they cross the line, by all means, end the call right away.</p>
            </div>

            <div class="col-12" style="padding: 2rem;">
                <h3 class="h4">Step 2: Remain calm.</h3>
                <p>It is important to stay calm during a call to prevent the situation from escalating. Here are a few tips to try:</p>

                <ul style="list-style:circle; list-style-position: inside;">
                    <li>Focus on using an even tone while you speak, remaining calm even if the customer continues to lose their temper. Using an agitated or angry tone will simply infuriate the caller even more.</li>
                    <li>Remember that the customer isn’t angry at you. It is the situation that is making the caller angry.</li>
                    <li>Put yourself in their shoes. How would you feel if this happened to you? Most customers have valid reasons for reacting to the situation with anger and impatience. Having a little bit of empathy for the customer’s feelings can go a long way.</li>
                    <li>Take a few deep breaths to calm your nerves while the caller is talking. Upset customers usually just need to tell someone their frustrations, so it often helps if you simply stay calm and listen.</li>
                </ul>
            </div>

            <div class="col-12" style="padding: 2rem;">
                <h3 class="h4">Step 3: Repeat information.</h3>
                <p>When the angry caller has finished talking, it is important to make them feel as if you are on their side. One way to do this is to apologize about the problem, convey empathy and then summarize their main points. Doing so will not only make the caller feel you are listening to them, it will also give them a few moments to calm down. You may be surprised at how much calmer they are when they start talking again.</p>
            </div>

            <div class="col-12" style="padding: 2rem;">
                <h3 class="h4">Step 4: Avoid the hold button.</h3>
                <p>Many customer service representatives will put an angry caller on hold, believing that the hold time will allow them a moment to calm down. Actually, the opposite is true. The hold time will add to the caller’s frustration, escalating the situation. Some callers believe that hold time allows representatives to talk rudely without being heard. Others believe that they are put on hold so that the representative doesn’t have to deal with the problem. While this may not be true, while on hold, a caller’s imagination will have plenty of time to think up negative reasons for why they are on hold. Instead of putting the caller on hold while you research the problem, talk to them. Let them know exactly what you are doing to solve their problem. For example, “it seems to me that you may have a billing error. I’m pulling up your bill, Ms. Jones, and taking a look. I see that you were billed on the first of the month and the payment was made automatically through your credit card. You said you changed your method of payment, so now I am going to take a look at the notes on your account to see when that change was made.” Talking to your angry customer will help to calm them as they will feel you care enough to guide them through the entire process.</p>
            </div>

            <div class="col-12" style="padding: 2rem;">
                <h3 class="h4">Step 5: Make the caller happy.</h3>
                <p>Once you have researched the problem, it is time to take action. Below are tips to help make the callers happy:</p>
            </div>
        </div>

        <div class="row">
            <div class="col-12" style="padding: 2rem;">
                <h3 class="h4">Give them options.</h3>
                <p>Giving your callers options to resolve their issue will make them feel more in control. Be sure to not only tell your customer what their options are but also how soon each option can be implemented. A lack of control can enrage the customer even further.</p>
            </div>

            <div class="col-12" style="padding: 2rem;">
                <h3 class="h4">Offer a refund or voucher.</h3>
                <p>If your company policy allows, offer them a refund or voucher. It can go a long way in showing that you are sorry and that you appreciate their business.</p>
            </div>

            <div class="col-12" style="padding: 2rem;">
                <h3 class="h4">Go the extra mile.</h3>
                <p>Before ending the call, ask the caller if there is anything else you can help them with. Going the extra mile can make them feel more appreciated.</p>
            </div>

            <div class="col-12" style="padding: 2rem;">
                <h3 class="h4">Make them smile.</h3>
                <p>Finally, compliment them on being patient or telling a joke to lighten the mood. If you can put a smile on their face before you end the call, you are an all-star agent.</p>
                <p>Handling angry callers are part of the job at any call center. Knowing how to deal with these challenging interactions effectively can go a long way in improving the customer’s experience with you and your company. Listening, staying calm, repeating information, avoiding the hold button, and making your caller happy are all essential when you have an angry caller on the line. Employing these techniques will not only allow you to help the customer but will also improve customer satisfaction and reduce handle times.</p>
