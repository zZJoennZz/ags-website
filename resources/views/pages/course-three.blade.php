                <h3 class="h4">Part A is reading</h3>
                <p>You will be instructed to read sentences included in the piece of paper given to you at the start of the test. The voice will tell you to read sentence 1, 10, 2, and 5 for instance; but even if you don’t read the sentences you are asked to read, it doesn’t matter. What matters is you read something loud and there is no dead air.</p>
