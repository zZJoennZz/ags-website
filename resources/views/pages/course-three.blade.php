                <p>For example, the voice says, “Andrew is thirsty, will he grab melon juice or an apple pie?” Even if your answer is “apple pie,” it doesn’t matter. Just make sure that you sound affirmative and you answer promptly. But of course it is better if you answer it truthfully and correctly.</p>
            </div>

