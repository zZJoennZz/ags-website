                <p>You just have to repeat the sentences you heard the voice say. Even if you don’t understand what has been spoken, it doesn’t really matter. Still say something in a way that you don’t sound unsure of what you are saying. Even if you say, “I’m sorry I don’t understand what you said because it was too fast.” is fine for as long as you follow the correct intonation and you don’t sound doubtful of your answer.</p>
