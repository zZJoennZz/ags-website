                <h3 class="h4">Part D is arranging the jumbled sentence</h3>
                <p>For example, “Robert believes / the difficult exam/ that he will pass.” should be arranged to “Robert believes that he will pass the difficult exam.” Again here it doesn’t matter if you are able to arrange the jumbled sentence correctly. Even if you say “Robert believes the difficult exam that he will pass.” in a manner that it sounds like it is the sentence itself, it is absolutely fine. In fact, even if you say a totally different sentence, it is OK.</p>
